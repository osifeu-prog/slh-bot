# התאמת מקלדת אנגלית ←→ עברית
EN_TO_HE = {
    't': 'א', 'u': 'ב', 'k': 'ג', 'a': 'ד', 'h': 'ה', 'v': 'ו', 'z': 'ז',
    'x': 'ח', 'i': 'ט', 'e': 'י', 'l': 'כ', 'f': 'ל', 'n': 'מ', 'o': 'נ',
    's': 'ס', 'y': 'ע', 'p': 'פ', 'm': 'צ', 'c': 'ק', 'r': 'ר', 'd': 'ש',
    'b': 'ת', 'g': 'ך', 'j': 'ם', 'w': 'ן', 'q': 'ף', ';': 'ץ',
    # capital letters
    'T': 'א', 'U': 'ב', 'K': 'ג', 'A': 'ד', 'H': 'ה', 'V': 'ו', 'Z': 'ז',
    'X': 'ח', 'I': 'ט', 'E': 'י', 'L': 'כ', 'F': 'ל', 'N': 'מ', 'O': 'נ',
    'S': 'ס', 'Y': 'ע', 'P': 'פ', 'M': 'צ', 'C': 'ק', 'R': 'ר', 'D': 'ש',
    'B': 'ת', 'G': 'ך', 'J': 'ם', 'W': 'ן', 'Q': 'ף'
}

def convert_to_hebrew(text):
    return ''.join(EN_TO_HE.get(ch, ch) for ch in text)
